"""1. Add, Subtract, Multiply, Divide Two Numbers"""
alpha = 10
beta = 5
print(alpha+beta)
print(alpha-beta)
print(alpha*beta)
print(alpha/beta)

"""2. Check if a Number is Even or Odd"""
number = 4
if number%2 == 0:
    print("Even")
else:
    print("Odd")

"""3. Calculate the Area of a Circle"""
radius = 5
area = 3.14*radius*radius
print(area)

"""4. Round a Float to 2 Decimal Places"""
value = 3.14159
print(round(value, 2))

